    public static getValue1D(ruleIndex:number, a:number, b:number, c:number):number{

        let d = parseInt(a+""+b+""+c, 2);//10進数にする(0-7まで)

        //ruleIndex(0-255)を8桁の２新数文字列にする
        let binary = ruleIndex.toString(2).padStart(8, '0');

        //if(Math.random()<0.001) console.log(binary);

        return parseInt(binary.substring(7-d,7-d+1));
        
    }

